##Listas

# Criando uma lista

frutas = ["maçã", "banana", "laranja"]

# Inserção
frutas.append("manga")  # Adiciona no final
frutas.insert(0, "abacaxi")  # Adiciona no início

# Remoção
frutas.remove("banana")  # Remove o primeiro "banana"
frutas.pop(0)  # Remove o primeiro elemento

# Acesso
print(frutas[0])

