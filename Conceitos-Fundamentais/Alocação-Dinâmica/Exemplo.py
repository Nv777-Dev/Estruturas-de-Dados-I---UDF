## Alocação dinâmica com uma lista que cresce conforme necessário

dynamic_memory = []  # Inicialmente vazia

dynamic_memory.append(1)
dynamic_memory.append(2)
dynamic_memory.append(3)

print(dynamic_memory)  # Saída: [1, 2, 3]

# Removendo elementos dinamicamente
dynamic_memory.pop()
print(dynamic_memory)  # Saída: [1, 2]

